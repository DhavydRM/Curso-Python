n = (input('Digite algo: '))
print(n.isnumeric()) #Verfica se é um número
print(n.isalpha()) #Verifica se é do alfabeto
print(n.isalnum()) #Verfia se é alfanumérico
print(n.isupper()) #Verfica se está só com letras MAIÚSCULAS
print(n.isascii()) #Verfica se todos os caracteres são ASCII
print(n.isdecimal()) #Verifica se todos os caracters são descimais(0 a 9)
print(n.isspace()) #Verifica se só tem espaços
print(n.islower()) #Verifica se está só com letras minúsculas
print(n.istitle()) #Verifica se está capitalizada(Com a primeira letra maiúscula)
